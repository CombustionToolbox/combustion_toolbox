function dydt=fnombrefuncion3(t,y,T,A,Ea,R)
k1=A(1)*exp(Ea(1)/(R*T));
k2=A(2)*T*exp(Ea(2)/(R*T));
k3=A(3);
dydt(7,1)= k3*y(5)*y(6);
dydt(6,1)= -k3*y(5)*y(6);
dydt(5,1)= k1*y(2)*y(4)-k2*y(5)*y(3)-k3*y(5)*y(6);
dydt(4,1)= -k1*y(2)*y(4);
dydt(3,1)= -k2*y(5)*y(3);
dydt(2,1)= -k1*y(2)*y(4)+k2*y(5)*y(3);
dydt(1,1)= k1*y(2)*y(4)+k2*y(5)*y(3)+k3*y(5)*y(6);